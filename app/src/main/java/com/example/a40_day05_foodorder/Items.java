package com.example.a40_day05_foodorder;

public class Items {
    String name;
    String imageURL;
    int price;
    int amount;

    public Items(String name, String imageURL, int price, int amount) {
        this.name = name;
        this.imageURL = imageURL;
        this.price = price;
        this.amount = amount;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImageURL() {
        return imageURL;
    }

    public void setImageURL(String imageURL) {
        this.imageURL = imageURL;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }
}
